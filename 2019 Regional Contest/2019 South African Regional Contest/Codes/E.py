morse = {'a':'.-', 'b':'-...', 'c':'-.-.', 'd': '-..', 'e': '.', 'f': '..-.', 'g': '--.', 'h': '....', 'i':'..', 'j': '.---', 'k': '-.-', 'l': '.-..', 'm': '--', 'n': '-.', 'o': '---', 'p': '.--.', 'q': '--.-', 'r': '.-.', 's': '...', 't': '-', 'u': '..-', 'v': '...-', 'w': '.--', 'x': '-..-', 'y': '-.--', 'z': '--..'}
while True:
    x = input()
    if x == '-1':
        break
    s = ''
    for i in x:
        s += morse[i]
    if s == s[::-1]:
        print('yes')
    else:
        print('no')
